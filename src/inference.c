uint64_t tmp[4] = {0};
float64_t float_tmp[4] = {0.0};

/*
 * returns probabilities * 2^32
 */
void predict(q15_t *fft_magnitudes, uint16_t *output)
{
    int32_t downsampled[7] = {0};
    downsampled[0] = fft_magnitudes[0];
    int i;
    for (i = 1; i < 31; i++)
    {
        downsampled[((i - 1) / 5) + 1] += (int32_t)fft_magnitudes[i];
    }

    int32_t z;

    for (i = 0; i < 4; i++)
    {
        tmp[i] = 1;
    }

    /*
     * max value would be (2^9)^7 == 2^63, so uint64 won't overflow
     * scale factor is 2^70
     */
    for (i = 0; i < 7; i++)
    {
        z = (abs(downsampled[i] - JOGGING_MU[i]) << 7) / JOGGING_SIGMA[i];
        if (z >= 384)
        {
            z = 383;
        }
        tmp[0] *= Z[z];

        z = (abs(downsampled[i] - SITTING_MU[i]) << 7) / SITTING_SIGMA[i];
        if (z >= 384)
        {
            z = 383;
        }
        tmp[1] *= Z[z];

        z = (abs(downsampled[i] - STATIONARY_MU[i]) << 7) / STATIONARY_SIGMA[i];
        if (z >= 384)
        {
            z = 383;
        }
        tmp[2] *= Z[z];

        z = (abs(downsampled[i] - WALKING_MU[i]) << 7) / WALKING_SIGMA[i];
        if (z >= 384)
        {
            z = 383;
        }
        tmp[3] *= Z[z];
    }

    float64_t total = 0.0;
    // possibly overflows (but highly unlikely)
    for (i = 0; i < 4; i++)
    {
        float_tmp[i] = (float64_t)tmp[i];
        total += float_tmp[i];
    }
    for (i = 0; i < 4; i++)
    {
        float_tmp[i] /= total;
        output[i] = (uint16_t)(float_tmp[i] * 1024.);
    }
}